Script::Script(
) : m_updateFuncID(-1),
	m_script(nullptr),
	m_scriptSize(0),
	m_context(nullptr),
	m_module(nullptr)
{
}

// -----------------------------------------------------------------------------

Script::~Script()
{
	SAFE_RELEASE(m_context);
}

// -----------------------------------------------------------------------------

void 
Script::Load(
	content::ContentReader& reader
)
{
	m_scriptSize = reader.Size();
	m_script = reader.Read<char>(m_scriptSize);
}

